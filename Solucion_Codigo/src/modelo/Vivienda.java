
package modelo;
public class Vivienda {
    private int ropa;
    private int calzado;
    private int accesorios;

    public int getRopa() {
        return ropa;
    }

    public void setRopa(int ropa) {
        this.ropa = ropa;
    }

    public int getCalzado() {
        return calzado;
    }

    public void setCalzado(int calzado) {
        this.calzado = calzado;
    }

    public int getAccesorios() {
        return accesorios;
    }

    public void setAccesorios(int accesorios) {
        this.accesorios = accesorios;
    }
    
}
